#!/usr/bin/env -S bash -e

# Apps.
curl=$( command -v 'curl' )

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

run() { sshd; }

# -------------------------------------------------------------------------------------------------------------------- #
# SSHD.
# -------------------------------------------------------------------------------------------------------------------- #

sshd() {
  local d='/etc/ssh/sshd_config.d'; [[ ! -d "${d}" ]] && exit 1
  local f='00-sshd.local.conf'

  ${curl} -fsSLo "${d}/${f}" "https://uaik.github.io/conf/ssh/${f}"
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< RUNNING SCRIPT >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

run "$@"; exit 0
