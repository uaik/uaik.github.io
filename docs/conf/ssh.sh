#!/usr/bin/env -S bash -e

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

init() {
  # Apps.
  cat=$( command -v cat )

  # Run.
  sshd
}

# -------------------------------------------------------------------------------------------------------------------- #
# SSHD.
# -------------------------------------------------------------------------------------------------------------------- #

sshd() {
  local d='/etc/ssh/sshd_config.d'; [[ ! -d "${d}" ]] && exit 1
  local f='00-sshd.local.conf'

  ${cat} > "${d}/${f}" <<EOF
Port 8022
IgnoreRhosts yes
MaxAuthTries 2
PermitEmptyPasswords no
PermitRootLogin no
X11Forwarding no
EOF
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< RUNNING SCRIPT >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

init "$@"; exit 0
