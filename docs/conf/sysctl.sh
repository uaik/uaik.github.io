#!/usr/bin/env -S bash -e

# Apps.
curl=$( command -v 'curl' )

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

run() { sysctl; }

# -------------------------------------------------------------------------------------------------------------------- #
# SYSCTL.
# -------------------------------------------------------------------------------------------------------------------- #

sysctl() {
  local d='/etc/sysctl.d'; [[ ! -d "${d}" ]] && exit 1
  local f='00-sysctl.local.conf'

  ${curl} -fsSLo "${d}/${f}" "https://uaik.github.io/conf/sysctl/${f}"
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< RUNNING SCRIPT >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

run "$@"; exit 0
