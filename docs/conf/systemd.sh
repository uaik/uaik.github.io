#!/usr/bin/env -S bash -e
# -------------------------------------------------------------------------------------------------------------------- #

# Apps.
apt=$( command -v 'apt' )
awk=$( command -v 'awk' )
curl=$( command -v 'curl' )
mv=$( command -v 'mv' )
sed=$( command -v 'sed' )
shutdown=$( command -v 'shutdown' )
systemctl=$( command -v 'systemctl' )

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

run() { networkd && resolved && reboot; }

# -------------------------------------------------------------------------------------------------------------------- #
# SYSTEMD / NETWORKD.
# -------------------------------------------------------------------------------------------------------------------- #

networkd() {
  local d; d='/etc/systemd/network'; [[ ! -d "${d}" ]] && exit 1
  local e; mapfile -t e < <( ip -br l | ${awk} '$1 !~ "lo|vir|wl" { print $1 }' )
  for i in "${e[@]}"; do
    ${curl} -fsSLo "${d}/${i}.network" 'https://uaik.github.io/conf/systemd/dhcp.network.tpl' \
      && ${sed} -i -e "s|<#_name_#>|${i}|g" "${d}/${i}.network"
  done
  local s; s='systemd-networkd.service'; ${systemctl} enable ${s}
  [[ -f '/etc/network/interfaces' ]] && { ${mv} '/etc/network/interfaces' '/etc/network/interfaces.disable'; }
}

# -------------------------------------------------------------------------------------------------------------------- #
# SYSTEMD / RESOLVED.
# -------------------------------------------------------------------------------------------------------------------- #

resolved() {
  local s; s='systemd-resolved.service'
  ${apt} install --yes ${s} && ${systemctl} enable ${s}
}

# -------------------------------------------------------------------------------------------------------------------- #
# REBOOT.
# -------------------------------------------------------------------------------------------------------------------- #

reboot() {
  ${shutdown} -r now
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< RUNNING SCRIPT >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

run "$@"; exit 0
