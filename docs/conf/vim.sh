#!/usr/bin/env -S bash -e

# Apps.
curl=$( command -v 'curl' )

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

run() { vim; }

# -------------------------------------------------------------------------------------------------------------------- #
# VIM.
# -------------------------------------------------------------------------------------------------------------------- #

vim() {
  local d='/etc/vim'; [[ ! -d "${d}" ]] && exit 1
  local f='vimrc.local'

  ${curl} -fsSLo "${d}/${f}" "https://uaik.github.io/conf/vim/${f}"
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< RUNNING SCRIPT >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

run "$@"; exit 0
