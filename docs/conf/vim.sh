#!/usr/bin/env -S bash -e

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

init() {
  # Apps.
  cat=$( command -v cat )

  # Run.
  vim
}

# -------------------------------------------------------------------------------------------------------------------- #
# VIM.
# -------------------------------------------------------------------------------------------------------------------- #

vim() {
  local d='/etc/vim'; [[ ! -d "${d}" ]] && exit 1
  local f='vimrc.local'

  ${cat} > "${d}/${f}" <<EOF
filetype plugin on
syntax on
set nobackup
set nowritebackup
set noswapfile
set tabstop=2
set expandtab
set ruler
set mouse-=a
set paste
EOF
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< RUNNING SCRIPT >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

init "$@"; exit 0
